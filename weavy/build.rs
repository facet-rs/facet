#[cfg(feature = "jit")]
fn emit_task_native(out: &Path, generated: &Path) {
    println!("cargo:rerun-if-changed=stencils/task_ops.rs");

    let target = env::var("TARGET").expect("TARGET set by cargo");
    let obj = out.join("weavy_task_stencils.o");
    let src = Path::new("stencils/task_ops.rs");
    let rustc = env::var("RUSTC").unwrap_or_else(|_| "rustc".to_string());

    unsafe { env::set_var("RUSTC_BOOTSTRAP", "1") };
    let tailcall = compile_object(&rustc, &[], src, &obj, &target, true);
    if !tailcall {
        assert!(
            compile_object(&rustc, &[], src, &obj, &target, false),
            "rustc failed to compile Weavy task stencils"
        );
    }

    let bytes = fs::read(&obj).unwrap();
    let mut s = String::new();
    s.push_str("// Generated by build.rs from stencils/task_ops.rs (task frame lane).\n");
    for (name, sym) in TASK_NAMES {
        if *name == "JUMP_IF_ZERO" {
            let st = extract_stencil_n(&bytes, TASK_SYMBOLS, sym, &["weavy_zero", "weavy_nonzero"]);
            emit_n(&mut s, name, &format!("`{sym}` task stencil."), &st);
            emit_cont_group(&mut s, "JUMP_IF_ZERO_TAKEN_CONT", name, &st.cont_relocs[0]);
            emit_cont_group(
                &mut s,
                "JUMP_IF_ZERO_FALLTHROUGH_CONT",
                name,
                &st.cont_relocs[1],
            );
        } else {
            let st = extract_stencil(&bytes, TASK_SYMBOLS, sym, "weavy_cont");
            emit(&mut s, name, &format!("`{sym}` task stencil."), &st);
            emit_cont(&mut s, &format!("{name}_CONT"), name, &st);
        }
    }
    fs::write(generated, s).unwrap();
}

