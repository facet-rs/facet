//! Swift code generation for vox services, on the phon codec.
//!
//! For a service, [`generate_service`] emits one module: the named type definitions,
//! the per-service phon schemas/descriptors/lowered programs ([`phon_service`]), the
//! typed client ([`phon_client`]) and server ([`phon_server`]).
//!
//! The Message/Handshake wire envelopes are emitted separately by
//! [`phon_descriptor::generate_phon_wire`].

pub mod phon_client;
pub mod phon_descriptor;
pub mod phon_server;
pub mod phon_service;
pub mod types;

use facet_core::Shape;
use vox_types::ServiceDescriptor;

pub use types::{collect_named_types, generate_named_types};

/// A wire type to generate Swift code for: its Swift name and facet `Shape`. Used to
/// build the envelope/handshake type lists passed to [`phon_descriptor::generate_phon_wire`].
pub struct WireType {
    /// The Swift name for this type (e.g. "Message").
    pub swift_name: String,
    /// The facet `Shape` for this type.
    pub shape: &'static Shape,
}

/// Generate a complete Swift module for a service on the phon codec, including
/// named types, per-service phon schemas/descriptors/programs, and the typed
/// client and server.
/// This is the main entry point for Swift service code generation.
/// r[impl service-macro.is-source-of-truth]
pub fn generate_service(service: &ServiceDescriptor) -> String {
    validate_channel_rules(service);
    // r[impl transport.fd.capability]
    super::validate_no_fd_shapes(service, "Swift");

    let mut out = String::new();
    out.push_str("// @generated by vox-codegen\n");
    out.push_str("// DO NOT EDIT - regenerate with `cargo xtask codegen --swift`\n\n");
    out.push_str("import Foundation\nimport PhonEngine\nimport PhonIR\nimport PhonSchema\nimport VoxRuntime\n\n");
    let named = collect_named_types(service);
    // phon types don't need Codable (the descriptor codec encodes/decodes them; it also
    // can't always synthesize). Drop it like the wire types do.
    let typedefs = generate_named_types(&named)
        .replace(": Codable, Sendable, Error", ": Sendable, Error")
        .replace(": Codable, Sendable", ": Sendable");
    out.push_str(&typedefs);
    out.push('\n');
    out.push_str(&phon_service::generate_phon_service(service));
    out.push_str(&phon_client::generate_phon_client(service));
    out.push_str(&phon_server::generate_phon_server(service));
    out
}

fn validate_channel_rules(service: &ServiceDescriptor) {
    for method in service.methods {
        // r[impl rpc.channel.placement]
        assert!(
            !vox_types::shape_contains_channel(method.return_shape),
            "channels are not allowed in return types: {}.{}",
            method.service_name,
            method.method_name
        );
        // r[impl rpc.channel.no-collections]
        assert!(
            !vox_types::shape_contains_channel_in_collection(method.args_shape),
            "channels are not allowed inside collections: {}.{}",
            method.service_name,
            method.method_name
        );
        // r[impl rpc.channel.direct-args]
        assert!(
            !vox_types::shape_contains_indirect_channel_arg(method.args_shape),
            "channels are only allowed as direct method arguments: {}.{}",
            method.service_name,
            method.method_name
        );
    }
}

