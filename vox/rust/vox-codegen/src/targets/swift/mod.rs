    fn schema_closure_expr(bytes: &[u8]) -> String {
        let encoded = STANDARD.encode(bytes);
        let mut out = String::from("voxSchemaClosure(\"\"\"\n");
        for chunk in encoded.as_bytes().chunks(100) {
            out.push_str("          ");
            out.push_str(std::str::from_utf8(chunk).expect("base64 is utf-8"));
            out.push('\n');
        }
        out.push_str("          \"\"\")");
        out
    }

    fn parse_self_describing_unique_bundle(
        bytes: &[u8],
        expected_root: u64,
    ) -> vox_phon::SchemaBundle {
        let bundle = vox_phon::parse_schema_bytes(bytes).expect("schema closure parses");
        assert_eq!(bundle.root.0, expected_root);
        let mut seen = BTreeSet::new();
        for schema in &bundle.schemas {
            assert!(
                seen.insert(schema.id.0),
                "schema closure must not repeat type ID {:#x}",
                schema.id.0
            );
        }
        bundle
    }

