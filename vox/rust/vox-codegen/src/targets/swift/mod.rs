    fn generated_swift_uses_phon_schema_closures_and_canonical_method_ids() {
        let echo = method_descriptor::<(NestedOuter,), NestedOuter>(
            "SchemaSvc",
            "EchoNested",
            &["value"],
            &[Some(<NestedOuter as Facet>::SHAPE)],
            MethodDescriptorOptions {
                response_wire_shape: <Result<NestedOuter, vox_types::VoxError> as Facet>::SHAPE,
                doc: None,
            },
        );
        let methods = Box::leak(vec![echo].into_boxed_slice());
        let service = ServiceDescriptor {
            service_name: "SchemaSvc",
            methods,
            doc: None,
        };

        let generated = generate_service(&service);
        let args_root = vox_phon::schema_id_for_shape(echo.args_shape).expect("args schema id");
        let response_root =
            vox_phon::schema_id_for_shape(echo.response_wire_shape).expect("response schema id");
        let args_closure =
            vox_phon::schema_bytes_for_shape(echo.args_shape).expect("args schema closure");
        let response_closure = vox_phon::schema_bytes_for_shape(echo.response_wire_shape)
            .expect("response schema closure");
        let args_bundle = parse_self_describing_unique_bundle(&args_closure, args_root.0);
        let _response_bundle =
            parse_self_describing_unique_bundle(&response_closure, response_root.0);
        let nested_inner_id =
            vox_phon::schema_id_for_shape(<NestedInner as Facet>::SHAPE).expect("inner schema id");

        assert_eq!(args_bundle.root, args_root);
        assert!(
            args_bundle
                .schemas
                .iter()
                .any(|schema| schema.id == nested_inner_id),
            "args closure must carry transitively referenced NestedInner schema"
        );
        assert!(
            generated.contains(&format!(
                "    {}: PhonMethodSchemas(",
                hex_u64(crate::method_id(echo))
            )),
            "generated Swift method table must use the canonical Vox method ID:\n{generated}"
        );
        assert!(
            generated.contains(&format!("argsRoot: SchemaId({})", hex_u64(args_root.0))),
            "generated Swift must use the phon args schema ID:\n{generated}"
        );
        assert!(
            generated.contains(&format!(
                "responseRoot: SchemaId({})",
                hex_u64(response_root.0)
            )),
            "generated Swift must use the phon response schema ID:\n{generated}"
        );
        assert!(
            generated.contains(&format!(
                "argsSchemaClosure: {}",
                schema_closure_expr(&args_closure)
            )),
            "generated Swift must embed the self-contained phon args closure:\n{generated}"
        );
        assert!(
            generated.contains(&format!(
                "responseSchemaClosure: {}",
                schema_closure_expr(&response_closure)
            )),
            "generated Swift must embed the self-contained phon response closure:\n{generated}"
        );
    }

    #[test]
    // r[verify rpc.method-id.no-collisions]
    fn generated_swift_method_ids_include_service_name() {
        let alpha = method_descriptor::<(), ()>(
            "AlphaSvc",
            "echo",
            &[],
            &[],
            MethodDescriptorOptions {
                response_wire_shape: <Result<(), vox_types::VoxError> as Facet>::SHAPE,
                doc: None,
            },
        );
        let beta = method_descriptor::<(), ()>(
            "BetaSvc",
            "echo",
            &[],
            &[],
            MethodDescriptorOptions {
                response_wire_shape: <Result<(), vox_types::VoxError> as Facet>::SHAPE,
                doc: None,
            },
        );

        assert_ne!(crate::method_id(alpha), crate::method_id(beta));

        let alpha_methods = Box::leak(vec![alpha].into_boxed_slice());
        let beta_methods = Box::leak(vec![beta].into_boxed_slice());
        let alpha_service = ServiceDescriptor {
            service_name: "AlphaSvc",
            methods: alpha_methods,
            doc: None,
        };
        let beta_service = ServiceDescriptor {
            service_name: "BetaSvc",
            methods: beta_methods,
            doc: None,
        };

        let alpha_generated = generate_service(&alpha_service);
        let beta_generated = generate_service(&beta_service);
        let alpha_entry = format!(
            "    {}: PhonMethodSchemas(",
            hex_u64(crate::method_id(alpha))
        );
        let beta_entry = format!(
            "    {}: PhonMethodSchemas(",
            hex_u64(crate::method_id(beta))
        );

        assert!(
            alpha_generated.contains(&alpha_entry),
            "Alpha service must emit its service-qualified method ID:\n{alpha_generated}"
        );
        assert!(
            !alpha_generated.contains(&beta_entry),
            "Alpha service must not reuse Beta service's same-method-name ID:\n{alpha_generated}"
        );
        assert!(
            beta_generated.contains(&beta_entry),
            "Beta service must emit its service-qualified method ID:\n{beta_generated}"
        );
    }

    #[test]
    // r[verify rpc.caller]
    // r[verify rpc.service]
    // r[verify rpc.service.methods]
    // r[verify rpc.handler]
    // r[verify rpc.fallible]
    // r[verify rpc.fallible.caller-signature]
    // r[verify rpc.fallible.vox-error]
    // r[verify rpc.fallible.vox-error.outcome]
    // r[verify rpc.unknown-method]
    fn generated_swift_emits_rpc_caller_handler_and_dispatch_shapes() {
        let echo = method_descriptor::<(String,), String>(
            "TestSvc",
            "echo",
            &["message"],
            &[None],
            MethodDescriptorOptions {
                response_wire_shape: <Result<String, vox_types::VoxError> as Facet>::SHAPE,
                doc: None,
            },
        );
        let divide = method_descriptor::<(u64, u64), Result<u64, String>>(
            "TestSvc",
            "divide",
            &["lhs", "rhs"],
            &[None, None],
            MethodDescriptorOptions {
                response_wire_shape: <Result<u64, vox_types::VoxError<String>> as Facet>::SHAPE,
                doc: None,
            },
        );
        let methods = Box::leak(vec![echo, divide].into_boxed_slice());
        let service = ServiceDescriptor {
            service_name: "TestSvc",
            methods,
            doc: None,
        };

        let generated = generate_service(&service);
        let echo_id = hex_u64(crate::method_id(echo));
        let divide_id = hex_u64(crate::method_id(divide));

        assert!(
            generated.contains("public protocol TestSvcCaller: Sendable {")
                && generated.contains("    func echo(message: String) async throws -> String")
                && generated.contains(
                    "    func divide(lhs: UInt64, rhs: UInt64) async throws -> Result<UInt64, String>",
                ),
            "generated Swift must expose async caller methods matching the source service:\n{generated}"
        );
        assert!(
            generated.contains("public final class TestSvcClient: TestSvcCaller, Sendable {")
                && generated.contains("    private let connection: VoxLane")
                && generated
                    .contains("    public init(connection: VoxLane, timeout: TimeInterval? = 30.0)")
                && generated.contains("let payload = encodeVoxTyped(message, testSvc_echo_ArgsEncoder)")
                && generated.contains(&format!(
                    "methodId: {echo_id}, metadata: .null, payload: payload, timeout: timeout,"
                ))
                && generated.contains(&format!(
                    "schemaInfo: ClientSchemaInfo(methodSchemas: testSvcMethods[{echo_id}]!, registry: testSvcRegistry)"
                ))
                && generated.contains("let result: Result<String, VoxError<Infallible>> = try decodeVoxTyped(respDecoder, response)"),
            "generated Swift client must delegate request serialization and response handling to VoxLane:\n{generated}"
        );
        assert!(
            generated.contains("    case connectionShutdown")
                && !generated.contains("    case sessionShutdown"),
            "generated Swift wire errors must use connection-era vocabulary:\n{generated}"
        );
        assert!(
            generated.contains("public protocol TestSvcHandler: Sendable {")
                && generated.contains("    func echo(message: String) async throws -> String")
                && generated.contains(
                    "    func divide(lhs: UInt64, rhs: UInt64) async throws -> Result<UInt64, String>",
                ),
            "generated Swift handler must expose service methods with server-side argument types:\n{generated}"
        );
        assert!(
            generated.contains("public final class TestSvcDispatcher: ServiceDispatcher {")
                && generated.contains("    private let handler: TestSvcHandler")
                && generated.contains("    public init(handler: TestSvcHandler) { self.handler = handler }")
                && generated.contains("public func dispatch(methodId: UInt64, payload: [UInt8], requestId: UInt64, channels: [UInt64], registry: ChannelRegistry, schemaSendTracker: SchemaSendTracker, schemaReceiveTracker: SchemaTracker, taskTx: @escaping @Sendable (TaskMessage) -> Void) async")
                && generated.contains(&format!(
                    "case {echo_id}: await dispatch_echo(payload: payload, requestId: requestId, schemaSendTracker: schemaSendTracker, schemaReceiveTracker: schemaReceiveTracker, taskTx: taskTx)"
                ))
                && generated.contains(&format!(
                    "case {divide_id}: await dispatch_divide(payload: payload, requestId: requestId, schemaSendTracker: schemaSendTracker, schemaReceiveTracker: schemaReceiveTracker, taskTx: taskTx)"
                ))
                && generated.contains("let voxValue = try await handler.echo(message: args)")
                && generated.contains("let voxValue = try await handler.divide(lhs: args.0, rhs: args.1)")
                && generated.contains("case .failure(let e): voxResult = .failure(.user(e))"),
            "generated Swift dispatcher must route decoded args to the handler and reply through TaskMessage:\n{generated}"
        );
        assert!(
            generated.contains(
                "default: taskTx(.response(requestId: requestId, payload: encodeVoxError(.unknownMethod), methodId: methodId))"
            ),
            "generated Swift dispatcher must return a call-level UnknownMethod response for unrecognized method ids:\n{generated}"
        );
    }

    #[test]
    // r[verify schema.errors.call-level.callee]
    // r[verify schema.errors.call-level.caller]
    // r[verify schema.errors.same-peer-terminal]
    fn generated_swift_maps_schema_decode_failures_to_call_errors() {
        let echo = method_descriptor::<(NestedOuter,), NestedOuter>(
            "SchemaSvc",
            "EchoNested",
            &["value"],
            &[Some(<NestedOuter as Facet>::SHAPE)],
            MethodDescriptorOptions {
                response_wire_shape: <Result<NestedOuter, vox_types::VoxError> as Facet>::SHAPE,
                doc: None,
            },
        );
        let methods = Box::leak(vec![echo].into_boxed_slice());
        let service = ServiceDescriptor {
            service_name: "SchemaSvc",
            methods,
            doc: None,
        };

        let generated = generate_service(&service);

        assert!(
            generated.contains("guard let argsDecoder = schemaReceiveTracker.buildDecodeFn"),
            "generated Swift server must build a caller->callee args decode plan:\n{generated}"
        );
        assert!(
            generated.contains("encodeVoxError(.invalidPayload(\"no args schema advertised\"))"),
            "generated Swift server must reject missing args decode plans as call errors:\n{generated}"
        );
        assert!(
            generated.contains("do { args = try decodeVoxTyped(argsDecoder, payload) } catch {")
                && generated.contains("encodeVoxError(.invalidPayload(\"decode args\"))"),
            "generated Swift server must turn args decode failures into InvalidPayload responses:\n{generated}"
        );
        assert!(
            generated
                .contains("guard let respDecoder = connection.schemaReceiveTracker.buildDecodeFn"),
            "generated Swift client must build a callee->caller response decode plan:\n{generated}"
        );
        assert!(
            generated.contains(
                "throw VoxError<Infallible>.invalidPayload(\"no response schema advertised\")",
            ),
            "generated Swift client must surface response decode-plan failure on the call:\n{generated}"
        );
        assert_eq!(
            generated.matches("connection.call(").count(),
            1,
            "generated Swift client must issue one request attempt and not replay after local decode failure:\n{generated}"
        );
    }

    #[test]
    // r[verify schema.exchange.channels]
    // r[verify rpc.channel.discovery]
    fn generated_swift_emits_channel_schemas() {
        let subscribe = method_descriptor::<(Tx<NestedInner>, Rx<NestedInner>), ()>(
            "StreamSvc",
            "subscribe",
            &["output", "input"],
            &[
                Some(<NestedInner as facet::Facet>::SHAPE),
                Some(<NestedInner as facet::Facet>::SHAPE),
            ],
            MethodDescriptorOptions {
                response_wire_shape: <Result<(), vox_types::VoxError> as Facet>::SHAPE,
                doc: None,
            },
        );
        let methods = Box::leak(vec![subscribe].into_boxed_slice());
        let service = ServiceDescriptor {
            service_name: "StreamSvc",
            methods,
            doc: None,
        };

        let generated = generate_service(&service);
        let args_closure = vox_phon::schema_bytes_for_shape_with_auxiliary_roots(
            subscribe.args_shape,
            &[
                (
                    "channel.arg.0.tx.element",
                    <NestedInner as facet::Facet>::SHAPE,
                ),
                (
                    "channel.arg.1.rx.element",
                    <NestedInner as facet::Facet>::SHAPE,
                ),
            ],
        )
        .expect("args schema closure with channel auxiliary roots");
        assert!(
            generated.contains("channel.arg.0.tx.element"),
            "Tx<T> channel receive decode must use the named auxiliary root:\n{generated}"
        );
        assert!(
            generated.contains("channel.arg.1.rx.element"),
            "Rx<T> channel receive decode must use the named auxiliary root:\n{generated}"
        );
        assert!(
            generated.contains(&format!(
                "argsSchemaClosure: {}",
                schema_closure_expr(&args_closure)
            )),
            "channel element roots must be carried in argsSchemaClosure auxiliary roots:\n{generated}"
        );
        assert!(
            generated.contains("buildAuxiliaryDecodeFn"),
            "generated Swift must build channel item decode programs from advertised schemas:\n{generated}"
        );
        assert!(
            !generated.contains("ElementDecodeProgram"),
            "generated Swift must not emit local same-schema channel item decode programs:\n{generated}"
        );
        assert!(
            generated.contains("public struct NestedInner"),
            "named channel element types must be emitted as normal generated types:\n{generated}"
        );
        assert!(
            generated.contains("var channelIds: [UInt64] = []")
                && generated.contains("let outputWireIndex = channelIds.count")
                && generated.contains("channelIds.append(await connection.bindClientTxArg(output")
                && generated.contains("let inputWireIndex = channelIds.count")
                && generated.contains("channelIds.append(await connection.bindClientRxArg(input")
                && generated.contains("channels: channelIds,"),
            "generated Swift caller must discover direct channel args left-to-right and send that channel id list:\n{generated}"
        );
        assert!(
            generated.contains("let outputWireIndex = channelWireIndex(args.0)")
                && generated.contains("channels[outputWireIndex]")
                && generated.contains("let inputWireIndex = channelWireIndex(args.1)")
                && generated.contains("channels[inputWireIndex]"),
            "generated Swift dispatcher must resolve decoded channel wire indexes through Request.channels:\n{generated}"
        );
    }

