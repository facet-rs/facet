/// Error returned while lowering or executing a Weavy parser runtime plan.
#[derive(Clone, Debug, PartialEq, Eq)]
#[non_exhaustive]
pub enum WeavyParseError {
    /// Parser grammar was not prepared for table/runtime execution.
    WrongStage {
        /// Actual parser generation stage.
        stage: parser_ir::ParserGenerationStage,
    },
    /// A parse state id was not present in the generated table.
    MissingState {
        /// Missing parse state.
        state: parser_ir::ParseStateId,
    },
    /// A state block was not generated for a parse state.
    MissingStateBlock {
        /// Missing parse state.
        state: parser_ir::ParseStateId,
    },
    /// An action block was not generated for a table action.
    MissingActionBlock {
        /// Parse state selecting the action.
        state: parser_ir::ParseStateId,
        /// Table entry selecting the action row.
        entry_index: usize,
        /// Index within the table entry action list.
        action_index: usize,
        /// Action that needed a block.
        action: parser_ir::ParseAction,
    },
    /// A lexical mode id was not present in the generated table.
    MissingLexMode {
        /// Missing lexical mode.
        mode: parser_ir::LexModeId,
    },
    /// The Weavy runner referenced a missing block.
    MissingBlock {
        /// Missing Snark block.
        block: SnarkBlockId,
    },
    /// A symbolic Snark block did not have a dense execution ref.
    MissingDenseBlock {
        /// Missing Snark block.
        block: SnarkBlockId,
    },
    /// The dense Weavy runner referenced a missing block index.
    MissingDenseRuntimeBlock {
        /// Missing dense block index.
        block: usize,
    },
    /// Weavy runtime execution reached a multi-action cell.
    UnsupportedConflict {
        /// Parse state containing the conflict.
        state: parser_ir::ParseStateId,
        /// Lookahead selecting the conflict.
        lookahead: parser_ir::LookaheadSymbol,
        /// Number of actions in the cell.
        action_count: usize,
    },
    /// Weavy runtime execution does not yet execute external scanners.
    UnsupportedExternalScanner {
        /// Parse state requesting external scanner support.
        state: parser_ir::ParseStateId,
        /// Number of external candidates in the lexical mode.
        external_count: usize,
    },
    /// External scanner host returned an error.
    ExternalScannerError {
        /// Parse state requesting external scanner support.
        state: parser_ir::ParseStateId,
        /// External token being scanned.
        external: parser_ir::ExternalId,
        /// Scanner error text.
        message: String,
    },
    /// Weavy runtime execution does not support this terminal root.
    UnsupportedTerminal {
        /// Terminal id.
        terminal: parser_ir::TerminalId,
        /// Terminal spelling.
        spelling: String,
    },
    /// Weavy runtime execution does not support lexical symbol refs.
    UnsupportedLexicalSymbol {
        /// Unsupported lexical expression.
        expr: GrammarExprId,
    },
    /// Weavy runtime execution reached recovery.
    UnsupportedRecovery {
        /// Parse state whose recovery action was reached.
        state: parser_ir::ParseStateId,
    },
    /// The Weavy parser stack was empty.
    EmptyStack,
    /// No token matched at the current byte offset.
    NoToken {
        /// Parse state attempting lexing.
        state: parser_ir::ParseStateId,
        /// Input byte offset.
        byte_position: usize,
        /// Expected token spellings.
        expected: Vec<String>,
    },
    /// No table action existed for the selected lookahead.
    NoAction {
        /// Parse state selecting the action.
        state: parser_ir::ParseStateId,
        /// Selected lookahead.
        lookahead: parser_ir::LookaheadSymbol,
        /// Input byte offset.
        byte_position: usize,
    },
    /// A reduce action could not find its goto edge.
    MissingGoto {
        /// Parse state after popping children.
        state: parser_ir::ParseStateId,
        /// Reduced nonterminal.
        nonterminal: parser_ir::NonterminalId,
    },
    /// Accept was reached before all input bytes were consumed.
    TrailingInput {
        /// Current byte offset.
        byte_position: usize,
    },
    /// Accept reduced to a hidden root.
    AcceptedHiddenRoot,
    /// Accept left a non-extra stack entry outside the accepted root.
    UnreducedStackEntry {
        /// State carried by the leftover stack entry.
        state: parser_ir::ParseStateId,
    },
    /// Weavy program finished without accepting a tree.
    MissingAcceptedTree,
    /// No Weavy branch accepted the input.
    NoViableBranch {
        /// Number of failed or retired branches.
        failure_count: usize,
    },
    /// Multiple accepted branches produced different runtime trees.
    AmbiguousParse {
        /// Number of accepted branches.
        accepted_count: usize,
        /// Accepted runtime trees.
        accepted: Vec<String>,
    },
    /// Branch worklist exceeded the runtime execution limit.
    BranchStepLimit {
        /// Configured step limit.
        limit: usize,
    },
    /// The supplied incremental edit does not describe the old/new inputs.
    InvalidInputEdit {
        /// Validation failure text.
        message: String,
    },
    /// A non-Snark canonical Weavy op appeared in the parser runtime plan.
    UnsupportedCanonicalOp,
}

impl fmt::Display for WeavyParseError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::WrongStage { stage } => {
                write!(f, "parser grammar is at stage {stage:?}, not Productions")
            }
            Self::MissingState { state } => write!(f, "parse state {} is missing", state.get()),
            Self::MissingStateBlock { state } => {
                write!(f, "state block for parse state {} is missing", state.get())
            }
            Self::MissingActionBlock {
                state,
                entry_index,
                action_index,
                action,
            } => write!(
                f,
                "action block for state {} entry {entry_index} action {action_index} ({action:?}) is missing",
                state.get()
            ),
            Self::MissingLexMode { mode } => {
                write!(f, "lexical mode {} is missing", mode.get())
            }
            Self::MissingBlock { block } => write!(f, "Weavy block {} is missing", block.get()),
            Self::MissingDenseBlock { block } => {
                write!(f, "dense ref for Weavy block {} is missing", block.get())
            }
            Self::MissingDenseRuntimeBlock { block } => {
                write!(f, "dense Weavy block {block} is missing")
            }
            Self::UnsupportedConflict {
                state,
                lookahead,
                action_count,
            } => write!(
                f,
                "state {} lookahead {lookahead:?} has {action_count} actions; Weavy runtime path is deterministic",
                state.get()
            ),
            Self::UnsupportedExternalScanner {
                state,
                external_count,
            } => write!(
                f,
                "state {} needs {external_count} external scanner candidates",
                state.get()
            ),
            Self::ExternalScannerError {
                state,
                external,
                message,
            } => write!(
                f,
                "external scanner failed in state {} for external {}: {message}",
                state.get(),
                external.get()
            ),
            Self::UnsupportedTerminal { terminal, spelling } => write!(
                f,
                "terminal {} with spelling {spelling:?} is unsupported",
                terminal.get()
            ),
            Self::UnsupportedLexicalSymbol { expr } => {
                write!(f, "lexical expression {} is a symbol ref", expr.get())
            }
            Self::UnsupportedRecovery { state } => {
                write!(f, "state {} reached unsupported recovery", state.get())
            }
            Self::EmptyStack => write!(f, "Weavy parser stack was empty"),
            Self::NoToken {
                state,
                byte_position,
                expected,
            } => write!(
                f,
                "state {} could not lex at byte {}; expected {:?}",
                state.get(),
                byte_position,
                expected
            ),
            Self::NoAction {
                state,
                lookahead,
                byte_position,
            } => write!(
                f,
                "state {} has no action for {lookahead:?} at byte {}",
                state.get(),
                byte_position
            ),
            Self::MissingGoto { state, nonterminal } => write!(
                f,
                "state {} has no goto for nonterminal {}",
                state.get(),
                nonterminal.get()
            ),
            Self::TrailingInput { byte_position } => {
                write!(
                    f,
                    "accept reached with trailing input at byte {byte_position}"
                )
            }
            Self::AcceptedHiddenRoot => write!(f, "accept reduced to a hidden root"),
            Self::UnreducedStackEntry { state } => write!(
                f,
                "accept left an unreduced stack entry in state {}",
                state.get()
            ),
            Self::MissingAcceptedTree => write!(f, "Weavy program finished without accepting"),
            Self::NoViableBranch { failure_count } => {
                write!(
                    f,
                    "no Weavy branch accepted; {failure_count} branches failed"
                )
            }
            Self::AmbiguousParse {
                accepted_count,
                accepted,
            } => write!(
                f,
                "{accepted_count} Weavy branches accepted with different runtime trees: {accepted:?}"
            ),
            Self::BranchStepLimit { limit } => {
                write!(f, "Weavy branch step limit {limit} was exceeded")
            }
            Self::InvalidInputEdit { message } => write!(f, "invalid input edit: {message}"),
            Self::UnsupportedCanonicalOp => {
                write!(f, "non-intrinsic canonical Weavy op is unsupported here")
            }
        }
    }
}

impl Error for WeavyParseError {}

fn select_runtime_weavy_failure(failures: Vec<WeavyParseError>) -> WeavyParseError {
    let failure_count = failures.len();
    failures
        .into_iter()
        .max_by_key(|error| {
            runtime_weavy_failure_position(error)
                .map(|byte| (true, byte))
                .unwrap_or((false, 0))
        })
        .unwrap_or(WeavyParseError::NoViableBranch { failure_count })
}

fn runtime_weavy_failure_position(error: &WeavyParseError) -> Option<usize> {
    match error {
        WeavyParseError::NoToken { byte_position, .. }
        | WeavyParseError::NoAction { byte_position, .. }
        | WeavyParseError::TrailingInput { byte_position } => Some(*byte_position),
        _ => None,
    }
}

/// Lower prepared parser tables into an executable Weavy parser program.
fn lower_weavy_parser_program(
    parser: &parser_ir::ParserGrammar,
    table: &parser_ir::ParseTable,
) -> Result<WeavyParserProgram, WeavyParseError> {
    if parser.stage() != parser_ir::ParserGenerationStage::Productions {
        return Err(WeavyParseError::WrongStage {
            stage: parser.stage(),
        });
    }

    let state_blocks = table
        .states()
        .iter()
        .map(|state| SnarkBlockId::from_index(state.id().get() as usize))
        .collect::<Vec<_>>();
    let root = state_blocks
        .first()
        .copied()
        .ok_or(WeavyParseError::MissingState {
            state: parser_ir::ParseStateId::from_index(0),
        })?;
    let mut lowered = WeavyLowered {
        program: vec![WeavyOp::Control(ControlOp::CallBlock {
            block: root,
            base_offset: 0,
        })],
        blocks: BTreeMap::new(),
    };
    let mut action_blocks = vec![Vec::new(); table.states().len()];
    let mut next_block = state_blocks.len();

    for state in table.states() {
        let state_block = state_blocks[state.id().get() as usize];
        lowered.blocks.insert(
            state_block,
            vec![
                WeavyOp::Intrinsic(SnarkIntrinsic::Lex {
                    version: StackVersionId::from_index(0),
                    mode: LexModeId::from_index(state.lex_mode().get() as usize),
                    output: LookaheadTokenId::from_index(0),
                }),
                WeavyOp::Intrinsic(SnarkIntrinsic::DispatchActions {
                    version: StackVersionId::from_index(0),
                    state: ParseStateId::from_index(state.id().get() as usize),
                    lookahead: LookaheadTokenId::from_index(0),
                }),
            ],
        );

        for entry in state.entries() {
            let mut blocks = Vec::with_capacity(entry.actions().len());
            for action in entry.actions() {
                let block = SnarkBlockId::from_index(next_block);
                next_block += 1;
                blocks.push(SymbolicWeavyParserActionBlock {
                    action: *action,
                    block,
                });
                lowered.blocks.insert(
                    block,
                    action_program_for(state.id(), entry.lookahead(), *action),
                );
            }
            action_blocks[state.id().get() as usize].push(blocks);
        }
    }

    let block_refs = lowered.block_refs();
    let dense = resolve_lowered(lowered.clone()).map_err(|error| match error {
        weavy::ir::ResolveError::MissingBlock(block) => WeavyParseError::MissingBlock { block },
        _ => WeavyParseError::UnsupportedCanonicalOp,
    })?;
    let state_block_refs = state_blocks
        .iter()
        .map(|block| {
            block_refs
                .get(block)
                .copied()
                .ok_or(WeavyParseError::MissingDenseBlock { block: *block })
        })
        .collect::<Result<Vec<_>, _>>()?;
    let action_blocks = action_blocks
        .into_iter()
        .map(|rows| {
            rows.into_iter()
                .map(|blocks| {
                    blocks
                        .into_iter()
                        .map(|block| {
                            let block_ref = block_refs
                                .get(&block.block)
                                .copied()
                                .ok_or(WeavyParseError::MissingDenseBlock { block: block.block })?;
                            Ok(WeavyParserActionBlock {
                                action: block.action,
                                block_ref,
                            })
                        })
                        .collect::<Result<Vec<_>, _>>()
                })
                .collect::<Result<Vec<_>, _>>()
        })
        .collect::<Result<Vec<_>, _>>()?;
    let extra_node_index = RuntimeWeavyExtraNodeIndex::new(parser);

    Ok(WeavyParserProgram {
        lowered,
        dense,
        state_blocks,
        state_block_refs,
        action_blocks,
        extra_node_index,
    })
}

fn action_program_for(
    source_state: parser_ir::ParseStateId,
    _lookahead: parser_ir::LookaheadSymbol,
    action: parser_ir::ParseAction,
) -> Vec<SnarkWeavyOp> {
    match action {
        parser_ir::ParseAction::Shift { state, .. } => vec![
            WeavyOp::Intrinsic(SnarkIntrinsic::CommitLookahead {
                version: StackVersionId::from_index(0),
                lookahead: LookaheadTokenId::from_index(0),
            }),
            WeavyOp::Intrinsic(SnarkIntrinsic::Shift {
                version: StackVersionId::from_index(0),
                state: ParseStateId::from_index(state.get() as usize),
                lookahead: LookaheadTokenId::from_index(0),
            }),
        ],
        parser_ir::ParseAction::ShiftExtra => vec![
            WeavyOp::Intrinsic(SnarkIntrinsic::CommitLookahead {
                version: StackVersionId::from_index(0),
                lookahead: LookaheadTokenId::from_index(0),
            }),
            WeavyOp::Intrinsic(SnarkIntrinsic::ShiftExtra {
                version: StackVersionId::from_index(0),
                state: ParseStateId::from_index(source_state.get() as usize),
                lookahead: LookaheadTokenId::from_index(0),
            }),
        ],
        parser_ir::ParseAction::Reduce {
            production,
            metadata,
            symbol,
            child_count,
            dynamic_precedence,
        } => vec![WeavyOp::Intrinsic(SnarkIntrinsic::Reduce {
            version: StackVersionId::from_index(0),
            production: ProductionId::from_index(production.get() as usize),
            metadata: ProductionMetadataId::from_index(metadata.get() as usize),
            symbol: NonterminalId::from_index(symbol.get() as usize),
            child_count,
            dynamic_precedence,
            aliases: None,
        })],
        parser_ir::ParseAction::Accept {
            production,
            metadata,
            symbol,
            child_count,
            dynamic_precedence,
        } => vec![WeavyOp::Intrinsic(SnarkIntrinsic::Accept {
            version: StackVersionId::from_index(0),
            production: ProductionId::from_index(production.get() as usize),
            metadata: ProductionMetadataId::from_index(metadata.get() as usize),
            symbol: NonterminalId::from_index(symbol.get() as usize),
            child_count,
            dynamic_precedence,
        })],
        parser_ir::ParseAction::Recover => vec![WeavyOp::Intrinsic(SnarkIntrinsic::Recover {
            state: ParseStateId::from_index(source_state.get() as usize),
        })],
    }
}

