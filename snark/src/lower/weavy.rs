/// A domain intrinsic emitted by Snark lowering.
#[derive(Clone, Debug, PartialEq, Eq)]
#[non_exhaustive]
pub enum SnarkIntrinsic {
    /// Read the next token according to a Snark lexical mode.
    Lex {
        /// GLR stack version being lexed.
        version: StackVersionId,
        /// Lexical mode selected by the current parser state.
        mode: LexModeId,
        /// Branch-local lookahead token produced by lexing.
        output: LookaheadTokenId,
    },
    /// Placeholder for an external scanner call.
    ///
    /// This does not define Snark's scanner host ABI. The valid-symbol mask,
    /// cursor, mark-end, result-symbol, EOF, and serialization contracts belong
    /// to the scanner ABI layer before this intrinsic becomes executable.
    CallExternalScanner {
        /// GLR stack version being scanned.
        version: StackVersionId,
        /// Parser state whose valid-symbol set is being offered.
        state: ParseStateId,
        /// Valid-symbol mask offered to the scanner.
        valid_symbols: ValidSymbolSetId,
        /// Scanner state selected by Snark's parser runtime.
        scanner_state: ExternalScannerStateId,
        /// Serialized scanner snapshot before the call.
        before: Option<ScannerSnapshotId>,
        /// Serialized scanner snapshot after the call.
        after: Option<ScannerSnapshotId>,
        /// Branch-local lookahead accepted by the scanner.
        result: Option<LookaheadTokenId>,
    },
    /// Select parser actions for the branch-local lookahead.
    DispatchActions {
        /// GLR stack version being dispatched.
        version: StackVersionId,
        /// Parser state at the stack head.
        state: ParseStateId,
        /// Branch-local lookahead token.
        lookahead: LookaheadTokenId,
    },
    /// Commit a branch-local lookahead to that branch's cursor.
    CommitLookahead {
        /// GLR stack version whose lookahead is being consumed.
        version: StackVersionId,
        /// Lookahead token being consumed.
        lookahead: LookaheadTokenId,
    },
    /// Shift the current lookahead and enter another parser state.
    Shift {
        /// GLR stack version being shifted.
        version: StackVersionId,
        /// Parser state to enter after the shift.
        state: ParseStateId,
        /// Lookahead token shifted by this action.
        lookahead: LookaheadTokenId,
    },
    /// Shift an extra token without changing parser stack shape.
    ShiftExtra {
        /// GLR stack version consuming the extra.
        version: StackVersionId,
        /// Parser state to re-enter after consuming the extra.
        state: ParseStateId,
        /// Lookahead token consumed as an extra.
        lookahead: LookaheadTokenId,
    },
    /// Reduce a production into a nonterminal symbol.
    Reduce {
        /// GLR stack version being reduced.
        version: StackVersionId,
        /// Production being reduced.
        production: ProductionId,
        /// Production metadata applied by this reduction.
        metadata: ProductionMetadataId,
        /// Symbol emitted by the reduction.
        symbol: NonterminalId,
        /// Structural child count popped by this reduction.
        child_count: usize,
        /// Dynamic precedence applied by this reduction.
        dynamic_precedence: i32,
        /// Optional alias sequence applied to visible children.
        aliases: Option<AliasSequenceId>,
    },
    /// Split a GLR branch for ambiguous actions.
    SplitGlr {
        /// Conflict that caused the split.
        conflict: ConflictId,
        /// Source stack version.
        source: StackVersionId,
        /// Branches created by the split.
        branches: Vec<StackVersionId>,
    },
    /// Merge compatible GLR branches.
    MergeGlr {
        /// Surviving stack version.
        survivor: StackVersionId,
        /// Retired stack version.
        retired: StackVersionId,
        /// Merge compatibility key.
        key: StackMergeKeyId,
        /// Ranking retained for the survivor.
        ranking: BranchRankingId,
    },
    /// Retire a GLR branch that can no longer win.
    RetireBranch {
        /// Stack version being retired.
        version: StackVersionId,
        /// Why this branch was retired.
        reason: BranchRetireReason,
    },
    /// Recover through Snark's generated recovery path.
    Recover {
        /// Parser state whose recovery action is being executed.
        state: ParseStateId,
    },
    /// Accept the input by reducing the root production.
    Accept {
        /// GLR stack version being accepted.
        version: StackVersionId,
        /// Root production being finalized.
        production: ProductionId,
        /// Root production metadata.
        metadata: ProductionMetadataId,
        /// Root symbol.
        symbol: NonterminalId,
        /// Structural child count popped by the root reduction.
        child_count: usize,
        /// Dynamic precedence applied by the root reduction.
        dynamic_precedence: i32,
    },
    /// Emit an observable parser trace event.
    EmitTrace {
        /// Trace event id.
        event: TraceEventId,
    },
    /// Emit a visible syntax node or token event.
    EmitNode {
        /// Symbol represented by the emitted event.
        symbol: SymbolId,
    },
    /// Emit a structured runtime tree event.
    EmitTreeEvent {
        /// Tree event id.
        event: TreeEventId,
    },
    /// Emit a query capture into the query result sink.
    EmitCapture {
        /// Query plan that emitted the capture.
        query: QueryPlanId,
        /// Query capture id.
        capture: QueryCaptureId,
        /// Runtime tree node captured.
        node: TreeNodeId,
        /// Optional field associated with the capture.
        field: Option<FieldId>,
    },
    /// Run a lowered query plan over the current tree/event stream.
    RunQuery {
        /// Query plan id.
        query: QueryPlanId,
    },
}

/// Why a GLR branch was retired by Snark lowering/runtime.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
#[repr(u8)]
pub enum BranchRetireReason {
    /// The branch accepted the input and left the live work queue.
    Accepted,
    /// Another branch dominated this one.
    Dominated,
    /// The parse table had no viable action.
    NoAction,
    /// A runtime bound retired the branch.
    Limit,
}

/// Broad semantic lane for one Snark dialect intrinsic.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
#[non_exhaustive]
pub enum SnarkIntrinsicDomain {
    /// Lexical matching against the source input.
    Lexing,
    /// Hand-written Tree-sitter external scanner boundary.
    ExternalScanner,
    /// Table-driven LR action selection and stack changes.
    ParserControl,
    /// GLR branch lifecycle operations.
    GlrControl,
    /// Recovery/error handling.
    Recovery,
    /// Runtime tree/event materialization.
    Tree,
    /// Query/highlight capture evaluation.
    Query,
    /// Diagnostic trace emission.
    Trace,
}

/// How a Snark intrinsic is expected to become executable by Weavy.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
#[non_exhaustive]
pub enum SnarkIntrinsicLowering {
    /// Native Snark dialect operation with effects visible to Weavy passes.
    DialectOp,
    /// Snark-owned lowered lexer graph referenced by this intrinsic.
    LexerGraph,
    /// External host call that remains an optimization barrier.
    HostCallBarrier,
    /// Sink/event operation, normally removable when the sink is disabled.
    SinkOp,
}

/// Optimizer-facing metadata for one Snark dialect intrinsic.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct SnarkIntrinsicSemantics {
    /// Stable dialect/name identity.
    pub descriptor: IntrinsicDescriptor,
    /// Semantic lane used by optimizer and JIT planning.
    pub domain: SnarkIntrinsicDomain,
    /// How this intrinsic lowers beyond the canonical Snark dialect op.
    pub lowering: SnarkIntrinsicLowering,
    /// Conservative effect contract used for legal scheduling/fusion.
    pub effect: EffectContract,
}

impl SnarkIntrinsic {
    /// The Weavy dialect name used by Snark intrinsics.
    pub const DIALECT: &'static str = "snark.tree_sitter";

    /// Optimizer-facing semantic metadata for this Snark dialect op.
    #[must_use]
    pub fn semantics(&self) -> SnarkIntrinsicSemantics {
        let descriptor = self.descriptor();
        let (domain, lowering, effect) = match self {
            Self::Lex { .. } => (
                SnarkIntrinsicDomain::Lexing,
                SnarkIntrinsicLowering::LexerGraph,
                EffectContract::new()
                    .read_resource(EffectResource::Input("source"))
                    .write_resource(EffectResource::SideChannel("lookahead"))
                    .may_fail(),
            ),
            Self::CallExternalScanner { .. } => (
                SnarkIntrinsicDomain::ExternalScanner,
                SnarkIntrinsicLowering::HostCallBarrier,
                EffectContract::opaque()
                    .read_resource(EffectResource::Input("source"))
                    .read_resource(EffectResource::SideChannel("scanner_state"))
                    .write_resource(EffectResource::SideChannel("scanner_state"))
                    .write_resource(EffectResource::SideChannel("lookahead"))
                    .may_fail()
                    .calls_user_code(),
            ),
            Self::DispatchActions { .. } => (
                SnarkIntrinsicDomain::ParserControl,
                SnarkIntrinsicLowering::DialectOp,
                EffectContract::new()
                    .read_resource(EffectResource::SideChannel("parser_stack"))
                    .read_resource(EffectResource::SideChannel("lookahead")),
            ),
            Self::CommitLookahead { .. } => (
                SnarkIntrinsicDomain::ParserControl,
                SnarkIntrinsicLowering::DialectOp,
                EffectContract::new()
                    .read_resource(EffectResource::SideChannel("lookahead"))
                    .read_resource(EffectResource::SideChannel("parser_stack"))
                    .write_resource(EffectResource::SideChannel("branch_cursor"))
                    .write_resource(EffectResource::SideChannel("parser_stack")),
            ),
            Self::Shift { .. } => (
                SnarkIntrinsicDomain::ParserControl,
                SnarkIntrinsicLowering::DialectOp,
                EffectContract::new()
                    .read_resource(EffectResource::SideChannel("parser_stack"))
                    .read_resource(EffectResource::SideChannel("lookahead"))
                    .write_resource(EffectResource::SideChannel("parser_stack")),
            ),
            Self::ShiftExtra { .. } => (
                SnarkIntrinsicDomain::ParserControl,
                SnarkIntrinsicLowering::DialectOp,
                EffectContract::new()
                    .read_resource(EffectResource::SideChannel("lookahead"))
                    .read_resource(EffectResource::SideChannel("branch_cursor"))
                    .write_resource(EffectResource::SideChannel("branch_cursor")),
            ),
            Self::Reduce { .. } => (
                SnarkIntrinsicDomain::Tree,
                SnarkIntrinsicLowering::DialectOp,
                EffectContract::new()
                    .read_resource(EffectResource::SideChannel("parser_stack"))
                    .write_resource(EffectResource::SideChannel("parser_stack"))
                    .write_resource(EffectResource::Sink("tree_events")),
            ),
            Self::SplitGlr { .. } => (
                SnarkIntrinsicDomain::GlrControl,
                SnarkIntrinsicLowering::DialectOp,
                EffectContract::new()
                    .read_resource(EffectResource::SideChannel("parser_stack"))
                    .write_resource(EffectResource::SideChannel("parser_stack"))
                    .write_resource(EffectResource::SideChannel("glr_worklist")),
            ),
            Self::MergeGlr { .. } => (
                SnarkIntrinsicDomain::GlrControl,
                SnarkIntrinsicLowering::DialectOp,
                EffectContract::new()
                    .read_resource(EffectResource::SideChannel("parser_stack"))
                    .write_resource(EffectResource::SideChannel("parser_stack"))
                    .write_resource(EffectResource::SideChannel("glr_worklist")),
            ),
            Self::RetireBranch { .. } => (
                SnarkIntrinsicDomain::GlrControl,
                SnarkIntrinsicLowering::DialectOp,
                EffectContract::new()
                    .write_resource(EffectResource::SideChannel("parser_stack"))
                    .write_resource(EffectResource::SideChannel("glr_worklist")),
            ),
            Self::Recover { .. } => (
                SnarkIntrinsicDomain::Recovery,
                SnarkIntrinsicLowering::DialectOp,
                EffectContract::new()
                    .read_resource(EffectResource::Input("source"))
                    .write_resource(EffectResource::SideChannel("branch_cursor"))
                    .write_resource(EffectResource::SideChannel("parser_stack"))
                    .write_resource(EffectResource::Sink("tree_events"))
                    .may_fail(),
            ),
            Self::Accept { .. } => (
                SnarkIntrinsicDomain::ParserControl,
                SnarkIntrinsicLowering::DialectOp,
                EffectContract::new()
                    .read_resource(EffectResource::SideChannel("parser_stack"))
                    .read_resource(EffectResource::SideChannel("branch_cursor"))
                    .write_resource(EffectResource::Sink("tree_events"))
                    .may_fail(),
            ),
            Self::EmitTrace { .. } => (
                SnarkIntrinsicDomain::Trace,
                SnarkIntrinsicLowering::SinkOp,
                EffectContract::new().write_resource(EffectResource::Sink("parser_trace")),
            ),
            Self::EmitNode { .. } | Self::EmitTreeEvent { .. } => (
                SnarkIntrinsicDomain::Tree,
                SnarkIntrinsicLowering::SinkOp,
                EffectContract::new().write_resource(EffectResource::Sink("tree_events")),
            ),
            Self::EmitCapture { .. } => (
                SnarkIntrinsicDomain::Query,
                SnarkIntrinsicLowering::SinkOp,
                EffectContract::new().write_resource(EffectResource::Sink("query_captures")),
            ),
            Self::RunQuery { .. } => (
                SnarkIntrinsicDomain::Query,
                SnarkIntrinsicLowering::DialectOp,
                EffectContract::new()
                    .read_resource(EffectResource::Sink("tree_events"))
                    .write_resource(EffectResource::Sink("query_captures"))
                    .may_fail(),
            ),
        };
        SnarkIntrinsicSemantics {
            descriptor,
            domain,
            lowering,
            effect,
        }
    }
}

impl IntrinsicOp for SnarkIntrinsic {
    fn descriptor(&self) -> IntrinsicDescriptor {
        let name = match self {
            Self::Lex { .. } => "lex",
            Self::CallExternalScanner { .. } => "call_external_scanner",
            Self::DispatchActions { .. } => "dispatch_actions",
            Self::CommitLookahead { .. } => "commit_lookahead",
            Self::Shift { .. } => "shift",
            Self::ShiftExtra { .. } => "shift_extra",
            Self::Reduce { .. } => "reduce",
            Self::SplitGlr { .. } => "split_glr",
            Self::MergeGlr { .. } => "merge_glr",
            Self::RetireBranch { .. } => "retire_branch",
            Self::Recover { .. } => "recover",
            Self::Accept { .. } => "accept",
            Self::EmitTrace { .. } => "emit_trace",
            Self::EmitNode { .. } => "emit_node",
            Self::EmitTreeEvent { .. } => "emit_tree_event",
            Self::EmitCapture { .. } => "emit_capture",
            Self::RunQuery { .. } => "run_query",
        };
        IntrinsicDescriptor {
            dialect: Self::DIALECT,
            name,
        }
    }

    fn effect(&self) -> EffectContract {
        self.semantics().effect
    }
}

