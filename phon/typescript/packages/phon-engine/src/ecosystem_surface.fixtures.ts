const schema = (schemaId: bigint, kind: SchemaKind): Schema => ({ id: schemaId, typeParams: [], kind });
const variant = (name: string, index: number, payload: VariantPayload): Variant => ({ name, index, payload });
const unit = (): VariantPayload => ({ kind: "unit" });
const newtype = (inner: SchemaRef): VariantPayload => ({ kind: "newtype", ref: inner });
const structPayload = (fields: Field[]): VariantPayload => ({ kind: "struct", fields });

